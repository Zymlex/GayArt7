const GLYPH_WIDTH = 5;
const INTER_CHAR_GAP = 1;

/**
 * Render text string to pixel coordinates.
 * Returns array of { col, row } where col = week offset, row = day (0=Sun..6=Sat).
 */
export function textToPixels(text) {
  const upper = text.toUpperCase();
  const pixels = [];
  let colOffset = 0;

  for (const ch of upper) {
    const glyph = FONT[ch];
    if (!glyph) {
      colOffset += 3; // small gap for unknown chars
      continue;
    }

    for (let row = 0; row < 7; row++) {
      for (let col = 0; col < GLYPH_WIDTH; col++) {
        if (glyph[row][col] === "#") {
          pixels.push({ col: colOffset + col, row });
        }
      }
    }

    colOffset += GLYPH_WIDTH + INTER_CHAR_GAP;
  }

  return pixels;
}

/**
 * Estimate total width in columns for given text.
 */
export function textWidth(text) {
  const upper = text.toUpperCase();
  let width = 0;
  for (const ch of upper) {
    if (FONT[ch]) {
      width += GLYPH_WIDTH + INTER_CHAR_GAP;
    } else {
      width += 3;
    }
  }
  return width - INTER_CHAR_GAP; // no trailing gap
}

/**
 * Render ASCII preview of the text as it would appear on the contribution graph.
 */
export function renderPreview(pixels, totalColumns) {
  const grid = Array.from({ length: 7 }, () =>
    Array(totalColumns).fill(" ")
  );

  for (const { col, row } of pixels) {
    if (col < totalColumns) {
      grid[row][col] = "#";
    }
  }

  const days = ["Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat"];
  let output = "";
  for (let row = 0; row < 7; row++) {
    output += `${days[row]} │ ${grid[row].join("")}\n`;
  }
  return output;
}

